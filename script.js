document.addEventListener('DOMContentLoaded', () => {
    const topStoriesContainer = document.getElementById('topStories');
    const worldNewsContainer = document.getElementById('worldNews');
    const sportsNewsContainer = document.getElementById('sportsNews');
    const techNewsContainer = document.getElementById('techNews');
    const moreNewsContainer = document.getElementById('moreNews');
    const searchBar = document.getElementById('search-text');

    const displayNews = (container, articles) => {
        container.innerHTML = '';
        articles.forEach(article => {
            const newsCard = document.createElement('div');
            newsCard.classList.add('news-card');
            newsCard.innerHTML = `
                <img src="${article.image}" alt="${article.title}">
                <div class="news-card-content">
                    <h2 class="news-card-title">${article.title}</h2>
                    <p class="news-card-description">${article.description}</p>
                </div>
            `;
            container.appendChild(newsCard);
        });
    };

    const allArticles = [...topStories, ...worldNews, ...sportsNews, ...techNews, ...moreNews];

    searchBar.addEventListener('input', (e) => {
        const searchString = e.target.value.toLowerCase();
        const filteredArticles = allArticles.filter(article => 
            article.title.toLowerCase().includes(searchString) ||
            article.description.toLowerCase().includes(searchString)
        );
        displayNews(topStoriesContainer, filteredArticles);
        displayNews(worldNewsContainer, filteredArticles);
        displayNews(sportsNewsContainer, filteredArticles);
        displayNews(techNewsContainer, filteredArticles);
        displayNews(moreNewsContainer, filteredArticles);
    });

    displayNews(topStoriesContainer, topStories);
    displayNews(worldNewsContainer, worldNews);
    displayNews(sportsNewsContainer, sportsNews);
    displayNews(techNewsContainer, techNews);
    displayNews(moreNewsContainer, moreNews);
});